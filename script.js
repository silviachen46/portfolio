const worksList = document.getElementById("worksList");
const filterBar = document.getElementById("filterBar");
const lightbox = document.getElementById("lightbox");
const lightboxImg = document.getElementById("lightboxImg");
const lightboxMeta = document.getElementById("lightboxMeta");
const lightboxClose = document.getElementById("lightboxClose");

let activeTag = "all";

function getAllTags(data) {
  const tagSet = new Set();
  data.forEach(work => {
    (work.tags || []).forEach(tag => tagSet.add(tag));
  });
  return ["all", ...Array.from(tagSet)];
}

function renderFilters() {
  const tags = getAllTags(works);

  filterBar.innerHTML = tags.map(tag => `
    <button
      class="filter-btn ${tag === activeTag ? "active" : ""}"
      data-tag="${tag}"
    >
      ${tag === "all" ? "All" : tag}
    </button>
  `).join("");

  document.querySelectorAll(".filter-btn").forEach(btn => {
    btn.addEventListener("click", () => {
      activeTag = btn.dataset.tag;
      renderFilters();
      renderWorks();
    });
  });
}

function renderWorks() {
  const filteredWorks =
    activeTag === "all"
      ? works
      : works.filter(work => (work.tags || []).includes(activeTag));

  if (filteredWorks.length === 0) {
    worksList.innerHTML = `
      <div class="empty-state">No works found under this tag.</div>
    `;
    return;
  }

  worksList.innerHTML = filteredWorks.map((work, index) => `
    <article class="work-item">
      <div class="work-thumb-wrap">
        <img
          class="work-thumb"
          src="${work.image}"
          alt="Artwork ${index + 1}"
          data-image="${work.image}"
          data-description="${escapeHtml(work.description || "")}"
          data-tags="${escapeHtml((work.tags || []).join(" · "))}"
          data-music="${work.music || ""}"
          data-video="${work.video || ""}"
        />
      </div>
      <div class="work-info">
        ${work.description ? `<p class="work-description">${escapeHtml(work.description)}</p>` : ""}
        <div class="work-tags">
          ${(work.tags || []).map(tag => `<span class="tag">${escapeHtml(tag)}</span>`).join("")}
        </div>
      </div>
    </article>
  `).join("");

  bindImageClicks();
}

function bindImageClicks() {
  document.querySelectorAll(".work-thumb").forEach(img => {
    img.addEventListener("click", () => {
      const description = img.dataset.description || "";
      const tags = img.dataset.tags || "";
      const music = img.dataset.music || "";
      const video = img.dataset.video || "";

      let mediaHtml = "";

      if (music) {
        mediaHtml = `
          <iframe
            src="${music}"
            style="border-radius:12px;"
            width="100%"
            height="352"
            frameborder="0"
            allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture"
            loading="lazy">
          </iframe>
        `;
      }

      if (video) {
        mediaHtml = `
          <iframe
            src="${video}"
            style="border-radius:12px;"
            width="100%"
            height="320"
            frameborder="0"
            allowfullscreen
            loading="lazy">
          </iframe>
        `;
      }

      lightboxImg.src = img.dataset.image;
      lightboxMeta.innerHTML = `
  <div class="lightbox-content-row">
    <div class="lightbox-text-block">
      ${description ? `<div class="lightbox-description">${description}</div>` : ""}
      <div class="lightbox-tags-line">${tags}</div>
    </div>
    ${mediaHtml ? `<div class="lightbox-media-block">${mediaHtml}</div>` : ""}
  </div>
`;

      lightbox.classList.remove("hidden");
    });
  });
}

function escapeHtml(str) {
  return String(str || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

lightboxClose.addEventListener("click", () => {
  lightbox.classList.add("hidden");
});

lightbox.addEventListener("click", (e) => {
  if (e.target === lightbox) {
    lightbox.classList.add("hidden");
  }
});

document.addEventListener("keydown", (e) => {
  if (e.key === "Escape") {
    lightbox.classList.add("hidden");
  }
});

renderFilters();
renderWorks();